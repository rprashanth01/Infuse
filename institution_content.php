		<div id="institution_accreditation" class="panel-collapse collapse">
			<div class="panel-body">
				<p>
				Having obtained Certification for compliance to ISO 9001:1994 in the year 2000, MSRIT recognized 
				the importance of getting its academic programs assessed by an accreditation body. 
				By then, the National Board of Accreditation (NBA), established by AICTE, was actively engaged in 
				assessing the technical programs in India. Though accreditation by NBA was not mandatory at that time, 
				realizing the value of such an accreditation, MSRIT voluntarily submitted applications to the NBA for the 
				accreditation of several of its major UG programs. After assessment, in February 2001, 
				NBA accorded accreditation to all the major departments of the Institute for their UG programs. 
				Some programs received accreditation at level A for 3 years and some others, at level B for 3 years. 
				This accreditation process helped the Institute in understanding better its strengths and weaknesses and 
				thus in setting higher goals of quality for itself.
				</p>
				<p>
				At the end of the three years of the validity of the first accreditation by NBA, MSRIT again applied to NBA, 
				in the year 2004, for the accreditation of all of its eligible UG Programs. After assessment during June 2004, 
				NBA accorded accreditation to all the programs assessed by them.
				</p>
