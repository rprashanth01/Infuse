				<h3>Bachelor of Engineering (B. E.)</h3>
				The following 4 year Engineering programs are offered in MSRIT:
				
				<ul>
					<li>Biotechnology</li>
					<li>Chemical Engineering</li>
					<li>Civil Engineering</li>
					<li>Computer Science and Engineering</li>
					<li>Electrical and Electronics Engineering</li>
					<li>Electronics and Communications Engineering</li>
					<li>Industrial Engineering and Management</li>
					<li>Information Science and Engineering</li>
					<li>Instrumentation Technology</li>
					<li>Mechanical Engineering</li>
					<li>Medical Electronics</li>
					<li>Telecommunication Engineering</li>
				</ul>
				
				<br /><strong>Eligibility</strong><br />
				A candidate who has passed 10 + 2 pattern of examination (equivalent to the two year 
				Pre University in Karnataka) with Physics and Mathematics as compulsory subjects along with 
				Chemistry/ Biotechnology/ Computer Science/ Biology/ Electronics as optional subjects in the qualifying 
				examination is eligible to pursue a Bachelor of Engineering program at MSRIT. 
				The eligibility for admission is 40% marks in the qualifying examination in case of SC/ST, Category-1 and 
				OBC Category candidates. The marks obtained by the candidate in Biotechnology/ Computer Science/ Biology/ 
				Electronics in the qualifying examination will be considered in the place of Chemistry, in case the marks 
				obtained in Chemistry is less than the required aggregate percentage for the purpose of determination 
				of eligibility (and not for the purpose of determination of rank, with regards to Engineering/ Technology courses).
				
				<br /><br />
				
				<h3>Bachelor of Architecture (B. A.)</h3>
				A 5 year bachelor degree in Architecture is offered in MSRIT.
				
				<br /><br /><strong>Eligibility</strong><br />
				A candidate who has passed 10 + 2 pattern of examination (equivalent to the two year Pre University in Karnataka) 
				with Mathematics as one of the optional subjects and has secured a minimum 50% aggregate marks in all 
				subjects in the qualifying examination is eligible for admission to the Bachelor of Architecture program. 
				The eligibility for admission is 45% marks in the case of SC/ST, Category-1 and OBC Category candidates. 
				This provided that the candidate has appeared and secured a minimum of 40% marks in NATA 
				(for SC/ ST, Category-1, and OBC Category candidates also) conducted by the Council of Architecture.
				
